package variable.yryskeldi;

public class MaxValue {
    public  static  void main (String[] args) {
        byte maxByte = Byte.MAX_VALUE;
        short maxShort = Short.MAX_VALUE;
        int maxInt = Integer.MAX_VALUE;
        long maxLong = Long.MAX_VALUE;

        System.out.println("Макс значение byte: " + maxByte);
        System.out.println("Макс значение short: " + maxShort);
        System.out.println("Макс значение int: " + maxInt);
        System.out.println("Макс значение long: " + maxLong);
    }
}
