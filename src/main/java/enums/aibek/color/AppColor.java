package enums.aibek.color;

public class AppColor {
    public static void main(String[] args) {
        Color  myColor = Color.RED;

        switch (myColor){
            case RED:
                System.out.println("Сиз тандаган түс: КЫЗЫЛ ");
                break;
            case BLUE:
                System.out.println("Сиз тандаган түс: КӨК ");
                break;
            case GREEN:
                System.out.println("Сиз тандаган түс: ЖАШЫЛ ");
                break;

        }
    }
}
