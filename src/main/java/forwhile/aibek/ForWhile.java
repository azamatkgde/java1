package forwhile.aibek;

import java.util.Scanner;

public class ForWhile {
    public static void main(String[] args) {
        Scanner scanner = new Scanner(System.in ) ;
        for (int i = 10; i >= 1; i --){
            System.out.println("Тескери: " +i );

        }

        scanner.close() ;
    }
}
