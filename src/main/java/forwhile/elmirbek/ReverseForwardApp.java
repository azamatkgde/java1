package forwhile.elmirbek;

public class ReverseForwardApp
{
    public static void main(String[] args) {

        // здача сделать так чтобы цикл работало в обратном и в прмяом напрвлений

        for (int a = 10; a >= 0; a--) {
            System.out.println(a + " Обратный счет");
        }

        for (int a = 0; a <= 20; a++) {
            System.out.println(a + " Прямой счет");
        }
    }
    }